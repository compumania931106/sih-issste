    private void jTextField2FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextField2FocusLost
        if(jTextField2.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Ingrese codigo de producto", "Error", JOptionPane.ERROR_MESSAGE);
            jTextField2.requestFocus();
        }
        else if(jTextField2.getText().matches("[0-9]*") == false){
            JOptionPane.showMessageDialog(this, "Solo numeros, en el campo de codigo", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        else{
            Conexion_BD cn = new Conexion_BD();
            rs = cn.buscarMedicamento(jTextField2.getText());
            if(rs != null){
                
                try {
                    if(rs.first()){
                        bandera = true;
                        jTextField1.setText(rs.getString(2));
                        jTextField3.setText(rs.getString(3));
                        jTextField4.setText(rs.getInt(4) + "");
                        jTextField5.setText(rs.getString(5));       
                    
                        jTextField1.setEditable(false);
                        jTextField3.setEditable(false);
                        jTextField5.setEditable(false);
                    }else{bandera = false;}
                } catch (SQLException ex) {
                    Logger.getLogger(VentanaMedicamentos.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        
        System.out.print(bandera);
    }//GEN-LAST:event_jTextField2FocusLost

