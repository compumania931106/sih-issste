    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if(bandera == false){
            if(jTextField2.getText().equals("") || jTextField1.getText().equals("") || jTextField3.getText().equals("") ||
                jTextField4.getText().equals("") || jTextField5.getText().equals("")){
            JOptionPane.showMessageDialog(this, "No puede haber campos vacios", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else if(jTextField2.getText().matches("[0-9]*") == false){
            JOptionPane.showMessageDialog(this, "Solo numeros, en el campo de codigo", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else if(jTextField4.getText().matches("[0-9]*") == false){
            JOptionPane.showMessageDialog(this, "Solo numeros, en el campo de cantidad", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        else{
            Conexion_BD cn = new Conexion_BD();
        
            if(cn.InsertarMedicamentos(jTextField2.getText(), jTextField1.getText().toUpperCase(), jTextField3.getText().toUpperCase(), Integer.parseInt(jTextField4.getText()), jTextField5.getText().toUpperCase())){
                this.llenarTabla();
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                jTextField5.setText("");
            }
            else{System.out.println("FALLA");}
        }
        }
        else{
            
            
            Conexion_BD cn = new Conexion_BD();
            
            int resul = cn.actualizarCantidadMedicamento(Integer.parseInt(jTextField4.getText()), jTextField2.getText());
            
            if(resul == 1){
                this.llenarTabla();
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                jTextField5.setText("");
                jTextField1.setEditable(true);
                jTextField3.setEditable(true);
                jTextField5.setEditable(true);
            }
            
            
            
        }
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

